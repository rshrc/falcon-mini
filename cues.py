def transform_wake_word_cues(wake_word_cues_map):
    new_map = {"wake_word_cues": {}}

    for language, genders in wake_word_cues_map["wake_word_cues"].items():
        new_map["wake_word_cues"][language] = {}
        for gender, cues in genders.items():
            new_map["wake_word_cues"][language][gender] = {}
            for i, cue in enumerate(cues, 1):
                new_map["wake_word_cues"][language][gender][cue] = i

    return new_map


