def transform_cues():
    new_map = {}
    for key, value in cues_map.items():
        new_map[key] = {}
        for language, genders in value.items():
            new_map[key][language] = {}
            for gender, cues in genders.items():
                new_map[key][language][gender] = {}
                for i, cue in enumerate(cues, 1):
                    new_map[key][language][gender][cue] = i

    return new_map

